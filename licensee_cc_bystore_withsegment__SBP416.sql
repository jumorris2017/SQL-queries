  -- Total valid response counts by question
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_1' THEN sr.QSTN_ID END) AS Q2_1_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_2' THEN sr.QSTN_ID END) AS Q2_2_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_3' THEN sr.QSTN_ID END) AS Q2_3_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_4' THEN sr.QSTN_ID END) AS Q2_4_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_5' THEN sr.QSTN_ID END) AS Q2_5_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_6' THEN sr.QSTN_ID END) AS Q2_6_Response_Total
  ,COUNT(CASE WHEN sr.QSTN_ID = 'Q2_7' THEN sr.QSTN_ID END) AS Q2_7_Response_Total
  
  -- Total top box responses
  ,SUM(CASE WHEN sr.RSPNS_ID = '7' AND sr.QSTN_ID = 'Q2_1' THEN 1 ELSE 0 END) AS Q2_1_TB_Cnt
  ,SUM(CASE WHEN sr.RSPNS_ID = '7' AND sr.QSTN_ID = 'Q2_2' THEN 1 ELSE 0 END) AS Q2_2_TB_Cnt
  ,SUM(CASE WHEN sr.RSPNS_ID = '7' AND sr.QSTN_ID = 'Q2_3' THEN 1 ELSE 0 END) AS Q2_3_TB_Cnt
