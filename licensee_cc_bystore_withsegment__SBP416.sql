  ,SUM(SUM(CASE WHEN sr.QSTN_ID = 'Q2_2' AND sr.RSPNS_ID = '7' THEN 1 ELSE 0 END)) OVER(PARTITION BY st.AREA_NUM) AS Q2_2_AREA_TB_CNT
